USE SCHEMA CURATED;

CREATE OR REPLACE TABLE CURATED.FACT_CDR (
    CDR_KEY INT AUTOINCREMENT,
    CALL_ID INT,
    CALLER STRING,
    RECEIVER STRING,
    DURATION INT,
    REVENUE FLOAT,
    IS_INTERNATIONAL INT,
    CALL_DATE DATE,
    TOWER_ID STRING
);

CREATE OR REPLACE TABLE CURATED.DIM_TOWER (
    TOWER_ID STRING PRIMARY KEY,
    REGION STRING,
    CITY STRING
);

INSERT INTO CURATED.DIM_TOWER
SELECT DISTINCT
    TOWERID,
    REGION,
    CITY
FROM STAGING.STG_TOWERMASTER;

SELECT * FROM CURATED.DIM_TOWER;

// MERGE 

MERGE INTO CURATED.FACT_CDR F
USING STAGING.STG_CDR S
ON F.CALL_ID = S.CALL_ID

WHEN MATCHED THEN UPDATE SET
    F.DURATION = S.DURATION,
    F.REVENUE = S.REVENUE

WHEN NOT MATCHED THEN INSERT (
    CALL_ID, CALLER, RECEIVER, DURATION,
    REVENUE, IS_INTERNATIONAL, CALL_DATE, TOWER_ID
)
VALUES (
    S.CALL_ID,
    S.CALLER,
    S.RECEIVER,
    S.DURATION,
    S.REVENUE,
    S.IS_INTERNATIONAL,
    DATE(S.CALL_TIMESTAMP),
    S.TOWER_ID
);


