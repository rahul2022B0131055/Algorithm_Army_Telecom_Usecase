CREATE OR REPLACE PROCEDURE SEND_ALERT()
RETURNS STRING
LANGUAGE SQL
AS
$$
DECLARE CNT INT;
BEGIN

    SELECT COUNT(*) INTO :CNT
    FROM ANALYTICS.HIGH_USAGE_ALERT
    WHERE ALERT_TIME >= DATEADD(MINUTE, -5, CURRENT_TIMESTAMP);

    IF (CNT > 0) THEN
    
        CALL SYSTEM$SEND_EMAIL(
            'EMAIL_INT',
            'nawalmathur2@gmail.com',
            'High Usage Alert',
            'Some customers exceeded usage limit. Check Snowflake table.'
        );
        
    END IF;

    RETURN 'DONE';

END;
$$;

