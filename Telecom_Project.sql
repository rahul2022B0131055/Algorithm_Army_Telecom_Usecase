CREATE OR REPLACE TABLE STAGING.STG_CDR (
    CALLID INT,
    CALLER STRING,
    RECEIVER STRING,
    DURATION INT,
    CALL_TYPE STRING,
    TOWER_ID STRING,
    CALL_TIMESTAMP TIMESTAMP,
    REVENUE FLOAT,
    IS_INTERNATIONAL INT
);
INSERT INTO STAGING.STG_CDR
SELECT DISTINCT
    CALLID,
    CALLER,
    RECEIVER,
    NVL(DURATION, 0) AS DURATION,
    CALLTYPE,
    TOWERID,
    TRY_TO_TIMESTAMP(TIMESTAMP) AS CALL_TIMESTAMP,
    NVL(DURATION,0) * 0.02 AS REVENUE,
    CASE 
        WHEN CALLTYPE = 'INT' THEN 1 
        ELSE 0 
    END AS IS_INTERNATIONAL
FROM RAW.CDR_RAW
WHERE DURATION >= 0 OR DURATION IS NULL;

SELECT * FROM Staging.STG_CDR;

