CREATE OR REPLACE TABLE Staging.STG_TOWERMASTER AS
SELECT
    TRIM(TowerID) AS TowerID,

    INITCAP(TRIM(REGION)) AS Region,

    INITCAP(TRIM(CITY)) AS City,
FROM RAW.TOWER_MASTER
WHERE 
    TowerID IS NOT NULL;

SELECT * FROM Staging.STG_TOWERMASTER;

