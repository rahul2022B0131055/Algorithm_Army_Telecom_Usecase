CREATE OR REPLACE TABLE ANALYTICS.HIGH_USAGE_ALERT (
    CALLER STRING,
    TOTAL_DURATION INT,
    ALERT_TIME TIMESTAMP
);

INSERT INTO ANALYTICS.HIGH_USAGE_ALERT
SELECT 
    CALLER,
    SUM(DURATION) AS TOTAL_DURATION,
    CURRENT_TIMESTAMP
FROM CURATED.FACT_CDR
GROUP BY CALLER
HAVING SUM(DURATION) > 1000;

