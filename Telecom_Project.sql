USE SCHEMA STAGING;

CREATE OR REPLACE TABLE Staging.STG_CUSTOMERMASTER AS
SELECT DISTINCT
    
    REGEXP_REPLACE(PhoneNumber, '[^0-9]', '') AS PhoneNumber,
    INITCAP(CustomerName) AS CustomerName,
    CASE 
        WHEN UPPER(PlanType) LIKE '%PREPAID%' THEN 'PREPAID'
        WHEN UPPER(PlanType) LIKE '%POSTPAID%' THEN 'POSTPAID'
        WHEN UPPER(PlanType) LIKE '%VAT%' THEN 'VAT'
        WHEN UPPER(PlanType) LIKE '%DATA%' THEN 'VAT'
        ELSE 'UNKNOWN'
    END AS PLANTYPE,
    TRY_TO_NUMBER(REGEXP_SUBSTR(PlanType, '[0-9]+')) AS PlanPrice
FROM RAW.CUSTOMER_MASTER
WHERE LENGTH(REGEXP_REPLACE(PhoneNumber, '[^0-9]', '')) = 10;

SELECT * FROM Staging.STG_CUSTOMERMASTER;

